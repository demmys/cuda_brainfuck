__host__ void *short_leaf_host(void *args){
    short_leaf_loop(((ThreadArgs *)args)->res, ((ThreadArgs *)args)->idx);
    free((ThreadArgs *)args);

    return (void *)NULL;
}

__host__ void *long_leaf_host(void *args){
    long_leaf_loop(((ThreadArgs *)args)->res, ((ThreadArgs *)args)->idx);
