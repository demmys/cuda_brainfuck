    return (void *)NULL;
}

__global__ void short_leaf_kernel(int *res, int thread_num){
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    if(idx < thread_num){
        short_leaf_loop(res, idx);
    }
}

__global__ void long_leaf_kernel(int *res, int thread_num){
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    if(idx < thread_num){
        long_leaf_loop(res, idx);
    }
}

__host__ int *host_loop(int thread_num){
    int *res;
    int idx;
    ThreadArgs *args;
    pthread_t *threads;

    res = (int *)malloc(sizeof(int) * thread_num);
    threads = (pthread_t *)malloc(sizeof(pthread_t) * thread_num);

    stop_watch_start();
    for(idx = 0; idx < thread_num; idx++){
        args = (ThreadArgs *)malloc(sizeof(ThreadArgs));
        args->idx = idx;
        args->res = res;
        if(flags & F_OPT){
            pthread_create(&threads[idx], NULL, long_leaf_host, (void *)args);
        } else{
            pthread_create(&threads[idx], NULL, short_leaf_host, (void *)args);
        }
    }
    for(idx = 0; idx < thread_num; idx++){
        pthread_join(threads[idx], NULL);
    }
    stop_watch_stop();

    free(threads);
    return res;
}

__host__ int *kernel_loop(int thread_num){
    int *res_d, *res;

    cudaMalloc(&res_d, sizeof(int) * thread_num);

    stop_watch_start();
    if(flags & F_OPT){
        long_leaf_kernel<<<thread_num, 1>>>(res_d, thread_num);
    } else{
        short_leaf_kernel<<<thread_num, 1>>>(res_d, thread_num);
    }
    cudaThreadSynchronize();
    stop_watch_stop();

    res = (int *)malloc(sizeof(int) * thread_num);
    cudaMemcpy(res, res_d, sizeof(int) * thread_num, cudaMemcpyDeviceToHost);
    cudaFree(res_d);
    return res;
}

